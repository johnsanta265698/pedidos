/**
 * Esta clase sera la que establezca los atributos con los cuales contara cada uno de nuestros clientes
 */
export class Cliente{
    clienteID: number
    nombre: string
    apellido: string
    direccion: string

    constructor(){
        this.clienteID = this.clienteID
        this.nombre = this.nombre
        this.apellido = this.apellido
        this.direccion = this.direccion
    }
}